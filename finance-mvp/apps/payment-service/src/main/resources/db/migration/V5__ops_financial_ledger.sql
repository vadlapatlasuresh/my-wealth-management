-- approve. Requiring approval for EVERYTHING sounds safer but isn't: it gets routed around by busy
-- agents, and a control everyone works around is worse than a sane threshold everyone respects.
-- Worth re-deriving from the actual Stripe refund distribution before treating 2500 as settled.
INSERT INTO ops_finance_config (config_key, config_value, description) VALUES
